import { NextResponse } from "next/server";

export async function GET() {
  const appUrl = process.env.RENDER_EXTERNAL_URL || process.env.SITE_URL || "https://diagbot-dashboard.onrender.com";

  const vbsDesktop = [
    'Set WshShell = WScript.CreateObject("WScript.Shell")',
    'strDesktop = WshShell.SpecialFolders("Desktop")',
    'Set lnk = WshShell.CreateShortcut(strDesktop & "\\XENTRY DiagBot Pro.lnk")',
    `lnk.TargetPath = "${appUrl}"`,
    'lnk.WindowStyle = 1',
    'lnk.IconLocation = "shell32.dll,14" ',
    'lnk.Description = "XENTRY DiagBot Pro - Mercedes-Benz Teshis Sistemi"',
    'lnk.Save',
  ].join("\r\n");

  const vbsStartMenu = [
    'Set WshShell = WScript.CreateObject("WScript.Shell")',
    'strSM = WshShell.SpecialFolders("AllUsersStartMenu") & "\\XENTRY DiagBot Pro"',
    'Set fso = CreateObject("Scripting.FileSystemObject")',
    'If Not fso.FolderExists(strSM) Then fso.CreateFolder strSM',
    'Set lnk = WshShell.CreateShortcut(strSM & "\\XENTRY DiagBot Pro.lnk")',
    `lnk.TargetPath = "${appUrl}"`,
    'lnk.WindowStyle = 1',
    'lnk.IconLocation = "shell32.dll,14" ',
    'lnk.Description = "XENTRY DiagBot Pro - Mercedes-Benz Teshis Sistemi"',
    'lnk.Save',
  ].join("\r\n");

  // Encode VBS content as base64 to avoid escaping issues
  const vbsDesktopB64 = Buffer.from(vbsDesktop, "utf-8").toString("base64");
  const vbsStartMenuB64 = Buffer.from(vbsStartMenu, "utf-8").toString("base64");

  const lines: string[] = [
    "@echo off",
    "setlocal enabledelayedexpansion",
    "chcp 65001 >nul 2>&1",
    "title XENTRY DiagBot Pro - Kurulum",
    "color 0A",
    "mode con: cols=70 lines=35",
    "",
    "echo.",
    "echo  ============================================",
    "echo   XENTRY DiagBot Pro Kurulum Sihirbazi v2.2.0",
    "echo  ============================================",
    "echo.",
    "echo  Mercedes-Benz AI Teshis Sistemi",
    "echo.",
    "",
    "net session >nul 2>&1",
    "if %errorLevel% neq 0 (",
    "    color 0C",
    "    echo  [HATA] Bu kurulum yonetici haklari gerektirir!",
    "    echo.",
    "    echo  Lutfen dosyaya sag tiklayip",
    "    echo  Yonetici olarak calistir secin.",
    "    echo.",
    "    pause",
    "    exit /b 1",
    ")",
    "echo  [OK] Yonetici haklari dogrulandi.",
    "echo.",
    "",
    "echo  [1/5] Kurulum klasoru olusturuluyor...",
    `set "INSTALL_DIR=%ProgramFiles%\\XENTRY DiagBot Pro"`,
    `if not exist "!INSTALL_DIR!" (`,
    `    mkdir "!INSTALL_DIR!"`,
    `    echo         [OK] Klasor olusturuldu`,
    `) else (`,
    `    echo         [OK] Klasor zaten mevcut`,
    `)`,
    "echo.",
    "",
    "echo  [2/5] Masaustu kisayolu olusturuluyor...",
    // Write VBS from base64 using PowerShell
    `powershell -NoProfile -Command "[System.Text.Encoding]::UTF8.GetString([System.Convert]::FromBase64String('${vbsDesktopB64}')) | Out-File -FilePath '%TEMP%\\diagbot_desktop.vbs' -Encoding ASCII"`,
    `cscript //nologo "%TEMP%\\diagbot_desktop.vbs"`,
    `if exist "%USERPROFILE%\\Desktop\\XENTRY DiagBot Pro.lnk" (`,
    `    echo         [OK] Masaustu kisayolu basariyla olusturuldu!`,
    `) else (`,
    `    echo         [UYARI] Masaustu kisayolu olusturulamadi`,
    `)`,
    "echo.",
    "",
    "echo  [3/5] Baslat menusu girisi olusturuluyor...",
    `powershell -NoProfile -Command "[System.Text.Encoding]::UTF8.GetString([System.Convert]::FromBase64String('${vbsStartMenuB64}')) | Out-File -FilePath '%TEMP%\\diagbot_startmenu.vbs' -Encoding ASCII"`,
    `cscript //nologo "%TEMP%\\diagbot_startmenu.vbs"`,
    `if exist "%ProgramData%\\Microsoft\\Windows\\Start Menu\\Programs\\XENTRY DiagBot Pro\\XENTRY DiagBot Pro.lnk" (`,
    `    echo         [OK] Baslat menusu girisi basariyla olusturuldu!`,
    `) else (`,
    `    echo         [UYARI] Baslat menusu girisi olusturulamadi`,
    `)`,
    "echo.",
    "",
    "echo  [4/5] Temizlik...",
    `del /f /q "%TEMP%\\diagbot_desktop.vbs" >nul 2>&1`,
    `del /f /q "%TEMP%\\diagbot_startmenu.vbs" >nul 2>&1`,
    `echo         [OK] Gecici dosyalar temizlendi`,
    "echo.",
    "",
    "echo  [5/5] Kisayollar dogrulaaniyor...",
    `echo.`,
    `echo  Masaustu:`,
    `if exist "%USERPROFILE%\\Desktop\\XENTRY DiagBot Pro.lnk" (`,
    `    echo    [+] XENTRY DiagBot Pro.lnk  - BULUNDU`,
    `) else (`,
    `    echo    [-] XENTRY DiagBot Pro.lnk  - BULUNAMADI`,
    `)`,
    `echo  Baslat Menusu:`,
    `if exist "%ProgramData%\\Microsoft\\Windows\\Start Menu\\Programs\\XENTRY DiagBot Pro" (`,
    `    echo    [+] XENTRY DiagBot Pro klasoru - BULUNDU`,
    `) else (`,
    `    echo    [-] XENTRY DiagBot Pro klasoru - BULUNAMADI`,
    `)`,
    "echo.",
    "",
    "echo.",
    "echo  ============================================",
    "echo   KURULUM BASARIYLA TAMAMLANDI!",
    "echo  ============================================",
    "echo.",
    "echo  Simdi uygulamayi acmak ister misiniz? (E/H)",
    "echo.",
    'choice /c EH /n /m "  Seciminiz [E=Ac, H=Kapat]: "',
    `if %errorLevel% equ 1 start "" "${appUrl}"`,
    "echo.",
    "echo  Tesekkurler! XENTRY DiagBot Pro kullandiginiz icin.",
    "echo.",
    "timeout /t 5 >nul",
    "exit /b 0",
    "",
  ];

  const batContent = lines.join("\r\n");

  return new NextResponse(batContent, {
    status: 200,
    headers: {
      "Content-Type": "application/octet-stream",
      "Content-Disposition": "attachment; filename=\"KURULUM.bat\"",
      "Content-Length": String(Buffer.byteLength(batContent, "utf-8")),
    },
  });
}
